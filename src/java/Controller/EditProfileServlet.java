    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id"),
                firstName = request.getParameter("firstname"),
                lastName = request.getParameter("lastname"),
                email = request.getParameter("email"),
                confirmEmail = request.getParameter("confirmemail"),
                phone = request.getParameter("phone"),
                fullName = firstName + " " + lastName;
        DAO d = new DAO();
        if (d.Update_user(phone, email, firstName, lastName, fullName, Integer.parseInt(id))) {
            request.getRequestDispatcher("/acc").forward(request, response);
        } else {
            request.setAttribute("notify", "Failed");
            request.getRequestDispatcher("editprofile.jsp").forward(request, response);
        }
    }

