    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO d = new DAO();
        List<Product_Category> cList = d.getAllCategoryNoParents();
        request.setAttribute("data", cList);
        request.getRequestDispatcher("buy.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Cookie arr[] = request.getCookies();
        List<CartItem> cList = new ArrayList<>();
        DAO d = new DAO();
        int count = 0;
        List<ProductDetail> list = d.getAllProductDetails();
        List<ProductImg> img = d.getAllProduct_img();
        String txt = "";
        if (arr != null) {
            for (Cookie o : arr) {
                if (o.getName().equals("cart")) {
                    txt += o.getValue();
                }
            }
        }
        if (txt == "") {
            response.sendRedirect("/RloyalPRJ/home");
            return;
        }
        Cart c = null;
        String[] s = txt.split("\\.");
        for (String a : s) {
            String quantity = request.getParameter("num_" + count);
            String[] n = a.split(":");
            int id = Integer.parseInt(n[0]);
            System.out.println(id);
            System.out.println(quantity);
            if (quantity != null) {
                int num = Integer.parseInt(quantity);
                c = new Cart(id, list, img, num);
                cList.addAll(c.getItems());
            }
            count++;
        }
        String total = request.getParameter("total_Money");
        request.setAttribute("product", cList);
        request.setAttribute("total", total);
        doGet(request, response);
    }

